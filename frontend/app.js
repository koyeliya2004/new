const calculateDimensions = (structure, volume) => {
  if (structure === "Recharge pit") {
    const depth = 2;
    const side = Math.sqrt(volume / depth);
    return `${formatMeters(side)} √ó ${formatMeters(side)} √ó ${formatMeters(depth)}`;
  }
  if (structure === "Recharge trench") {
    const depth = 1.5;
    const width = 1.2;
    const length = volume / (depth * width);
    return `${formatMeters(length)} √ó ${formatMeters(width)} √ó ${formatMeters(depth)}`;
  }
  const radius = 0.6;
  const depth = volume / (Math.PI * radius * radius);
  return `${formatMeters(depth)} depth ‚Ä¢ ${formatMeters(radius * 2)} diameter`;
};

